describe('copyCode', () => {

    // Displays "Code Copied" with a tick mark
    it('should display "Code Copied" with a tick mark', () => {
      // Arrange
      const blockNumber = 1;
      const codeBlock = document.createElement('div');
      codeBlock.classList.add('code-block');
      const pre = document.createElement('pre');
      pre.textContent = 'const x = 10;';
      codeBlock.appendChild(pre);
      const copyButton = document.createElement('button');
      copyButton.classList.add('copy-button');
      codeBlock.appendChild(copyButton);
      document.body.appendChild(codeBlock);

      // Act
      copyCode(blockNumber);

      // Assert
      expect(codeBlock.querySelector('.copy-button').innerHTML).toBe('<span class="tick-mark"></span> Code Copied');

      // Clean up
      document.body.removeChild(codeBlock);
    });
});
